grant select on public.hq_archive_categories, public.hq_archive_subcategories, public.hq_archive_keywords,
  public.hq_archive_contents, public.hq_worldcups, public.hq_worldcup_candidates to anon, authenticated;
create or replace view public.hq_archive_public
with (security_invoker = true)
as
select
  content.id,
  content.title,
  content.occurred_on,
  content.source_url,
  content.account,
  category.name as main_category,
  subcategory.name as sub_category,
  content.keywords,
  content.thumbnail_url,
  content.updated_at
from public.hq_archive_contents content
left join public.hq_archive_categories category on category.id = content.category_id
left join public.hq_archive_subcategories subcategory on subcategory.id = content.subcategory_id
where content.status = 'published';
grant select on public.hq_archive_public to anon, authenticated;
create or replace view public.hq_worldcup_public
with (security_invoker = true)
as
select
  cup.id,
  cup.legacy_cup_id,
  cup.title,
  cup.icon,
  cup.sort_order,
  count(candidate.id)::bigint as candidate_count
from public.hq_worldcups cup
left join public.hq_worldcup_candidates candidate
  on candidate.worldcup_id = cup.id and candidate.is_active = true
where cup.is_active = true
group by cup.id, cup.legacy_cup_id, cup.title, cup.icon, cup.sort_order;
grant select on public.hq_worldcup_public to anon, authenticated;
create or replace function public.hq_archive_today(
  p_month_day text,
  p_offset integer default 0,
  p_limit integer default 30
)
returns table (
  id bigint,
  title text,
  occurred_on date,
  source_url text,
  account text,
  main_category text,
  sub_category text,
  keywords text[],
  thumbnail_url text,
  total_count bigint
)
language sql
stable
security invoker
as $$
  select
    archive.id, archive.title, archive.occurred_on, archive.source_url, archive.account,
    archive.main_category, archive.sub_category, archive.keywords, archive.thumbnail_url,
    count(*) over() as total_count
  from public.hq_archive_public archive
  where to_char(archive.occurred_on, 'MM-DD') = p_month_day
  order by archive.occurred_on desc, archive.id desc
  offset greatest(p_offset, 0)
  limit least(greatest(p_limit, 1), 60);
$$;
grant execute on function public.hq_archive_today(text, integer, integer) to anon, authenticated;
create or replace function public.hq_record_worldcup_win(p_candidate_id bigint)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  next_count bigint;
begin
  update public.hq_worldcup_candidates
  set win_count = win_count + 1
  where id = p_candidate_id and is_active = true
  returning win_count into next_count;
  if next_count is null then raise exception 'Candidate not found'; end if;
  return next_count;
end;
$$;
revoke all on function public.hq_record_worldcup_win(bigint) from public;
grant execute on function public.hq_record_worldcup_win(bigint) to anon, authenticated;
commit;
