begin;
create extension if not exists pg_trgm;
create or replace function public.hq_set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;
create table if not exists public.hq_archive_categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create table if not exists public.hq_archive_subcategories (
  id bigint generated by default as identity primary key,
  category_id bigint not null references public.hq_archive_categories(id) on delete cascade,
  name text not null,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (category_id, name),
  unique (id, category_id)
);
create table if not exists public.hq_archive_keywords (
  id bigint generated by default as identity primary key,
  name text not null unique,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create table if not exists public.hq_archive_contents (
  id bigint generated by default as identity primary key,
  legacy_source_key text unique,
  title text not null,
  occurred_on date,
  source_url text,
  account text,
  category_id bigint references public.hq_archive_categories(id) on delete set null,
  subcategory_id bigint,
  keywords text[] not null default '{}',
  thumbnail_url text,
  status text not null default 'published' check (status in ('draft', 'published')),
  source_type text not null default 'manual' check (source_type in ('csv', 'manual', 'twitter')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint hq_archive_contents_subcategory_fk
    foreign key (subcategory_id, category_id)
    references public.hq_archive_subcategories(id, category_id)
    on delete set null
);
create table if not exists public.hq_worldcups (
  id bigint generated by default as identity primary key,
  legacy_cup_id text unique,
  title text not null,
  icon text not null default '🏆',
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create table if not exists public.hq_worldcup_candidates (
  id bigint generated by default as identity primary key,
  worldcup_id bigint not null references public.hq_worldcups(id) on delete cascade,
  legacy_candidate_id text,
  name text not null,
  occurred_on date,
  source_url text,
  media_url text,
  media_type text check (media_type is null or media_type in ('image', 'video', 'text')),
  width integer not null default 0 check (width >= 0),
  height integer not null default 0 check (height >= 0),
  poster_url text,
  win_count bigint not null default 0 check (win_count >= 0),
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (worldcup_id, legacy_candidate_id)
);
