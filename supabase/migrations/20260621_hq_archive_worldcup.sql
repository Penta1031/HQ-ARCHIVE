create index if not exists hq_archive_contents_filter_idx on public.hq_archive_contents (status, category_id, subcategory_id, occurred_on desc);
create index if not exists hq_archive_contents_title_trgm_idx on public.hq_archive_contents using gin (title gin_trgm_ops);
